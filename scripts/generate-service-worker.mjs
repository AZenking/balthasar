import { createHash } from "node:crypto";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

function option(name) {
  const index = process.argv.indexOf(name);
  return index === -1 ? undefined : process.argv[index + 1];
}

const offlinePath = option("--offline") ?? path.resolve("public/offline.html");
const outputPath = option("--output") ?? path.resolve("public/sw.js");
const offline = await readFile(offlinePath);
const buildId = createHash("sha256").update(offline).update("pwa-shell-v1").digest("hex").slice(0, 12);

const worker = `/* Generated by scripts/generate-service-worker.mjs. */
const BUILD_ID = '${buildId}';
const CACHE_PREFIX = 'balthasar-pwa-';
const SHELL_CACHE = CACHE_PREFIX + 'shell-' + BUILD_ID;
const STATIC_CACHE = CACHE_PREFIX + 'static-' + BUILD_ID;
const SHELL_ASSETS = [
  "/offline.html",
  "/manifest.webmanifest",
  "/pwa/icon-192.png",
  "/pwa/icon-512.png",
  "/pwa/icon-maskable-512.png"
];

async function clients() {
  try { return await self.clients.matchAll({ includeUncontrolled: true }); }
  catch { return []; }
}

async function announceReady() {
  const list = await clients();
  for (const client of list) {
    try {
      client.postMessage({ type: 'WORKER_READY', buildId: BUILD_ID });
    } catch {
      // Client may have gone away; safe to ignore.
    }
  }
}

self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(SHELL_CACHE)
      .then((cache) => cache.addAll(SHELL_ASSETS))
      .then(() => announceReady())
      .catch((error) => {
        // Cache failure is reportable; the page can warn the user that the
        // offline shell will not be available after this load.
        const list = self.clients && self.clients.matchAll
          ? self.clients.matchAll({ includeUncontrolled: true })
          : Promise.resolve([]);
        return list.then((clients) => {
          for (const client of clients) {
            try {
              client.postMessage({
                type: 'CACHE_ERROR',
                buildId: BUILD_ID,
                resourceKind: 'shell',
              });
            } catch {
              // Swallow — never throw out of install handler.
            }
          }
          throw error;
        });
      })
  );
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((keys) => Promise.all(keys.map((key) =>
      key.startsWith(CACHE_PREFIX) && key !== SHELL_CACHE && key !== STATIC_CACHE
        ? caches.delete(key)
        : undefined
    ))).then(() => self.clients.claim()).then(() => announceReady())
  );
});

self.addEventListener('fetch', (event) => {
  const request = event.request;
  const url = new URL(request.url);
  if (request.method !== 'GET') return;
  if (
    url.origin !== self.location.origin ||
    url.pathname.startsWith('/api/') ||
    url.pathname.startsWith('/api/auth') ||
    url.pathname.startsWith('/api/trpc') ||
    request.headers.has('RSC') ||
    request.headers.has('Next-Router-State-Tree')
  ) return;

  if (request.mode === 'navigate') {
    event.respondWith(fetch(request).catch(() => {
      return caches.match('/offline.html');
    }));
    return;
  }

  if (SHELL_ASSETS.includes(url.pathname)) {
    event.respondWith(caches.match(request).then((cached) => cached || fetch(request)));
    return;
  }

  if (url.pathname.startsWith('/_next/static/')) {
    event.respondWith(caches.match(request).then(async (cached) => {
      if (cached) return cached;
      const response = await fetch(request);
      if (response.ok && response.status === 200) {
        const cache = await caches.open(STATIC_CACHE);
        try {
          await cache.put(request, response.clone());
        } catch (error) {
          const list = self.clients && self.clients.matchAll
            ? self.clients.matchAll({ includeUncontrolled: true })
            : Promise.resolve([]);
          list.then((clients) => {
            for (const client of clients) {
              try {
                client.postMessage({
                  type: 'CACHE_ERROR',
                  buildId: BUILD_ID,
                  resourceKind: 'static',
                });
              } catch {
                // Swallow — fetch still returns the response.
              }
            }
          });
        }
      }
      return response;
    }));
  }
});

self.addEventListener('message', (event) => {
  // Only activate the new worker when the page explicitly opts in AND the
  // buildId matches — prevents stale tabs from forcing an update that drops
  // an unsaved draft.
  if (
    event.data &&
    event.data.type === 'SKIP_WAITING' &&
    event.data.buildId === BUILD_ID
  ) {
    self.skipWaiting();
  }
});
`;

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, worker, "utf8");
